PROMPT_TEMPLATE = """
You are a retail customer-feedback analyst...
"""

def analyze_review(review_text, api_key=None):
    """
    Send one review to Gemini and return a Python dictionary.
    """

    if not review_text or not review_text.strip():
        raise ValueError("Please enter a customer review.")

    # First use the key passed by app.py.
    # Otherwise, look for an environment variable.
    key = api_key or os.getenv("GEMINI_API_KEY")

    if not key:
        raise ValueError(
            "Gemini API key is missing."
        )

    prompt = PROMPT_TEMPLATE.format(
        review=review_text.strip()
    )

    payload = {
        "contents": [
            {
                "role": "user",
                "parts": [
                    {
                        "text": prompt
                    }
                ]
            }
        ],
        "generationConfig": {
            "temperature": 0.1,
            "responseMimeType": "application/json",
            "responseSchema": RESPONSE_SCHEMA
        }
    }

    response = requests.post(
        API_URL,
        params={
            "key": key
        },
        headers={
            "Content-Type": "application/json"
        },
        json=payload,
        timeout=90
    )

    response.raise_for_status()

    raw_response = response.json()

    try:
        response_text = (
            raw_response["candidates"][0]
            ["content"]["parts"][0]["text"]
        )

        result = json.loads(response_text)

    except (
        KeyError,
        IndexError,
        json.JSONDecodeError
    ) as error:

        raise ValueError(
            f"Could not parse Gemini response: {raw_response}"
        ) from error

    return result

