def gradio_analyze_review(review_text):
    """
    Analyze one customer review and return the values required
    by the Gradio interface.
    """

    if not review_text or not review_text.strip():
        return (
            "⚠️ Please enter a customer review.",
            "",
            "",
            "",
            "",
            "",
            "",
            None
        )

    try:
        result = analyze_review(
            review_text.strip(),
            api_key=GEMINI_API_KEY
        )

        # Safeguard in case the function returns JSON text
        if isinstance(result, str):
            result = json.loads(result)

        if not isinstance(result, dict):
            raise TypeError(
                "The Gemini function did not return a dictionary."
            )

        category = result.get(
            "Category",
            "Not returned"
        )

        sentiment = result.get(
            "Sentiment",
            "Not returned"
        )

        urgency = result.get(
            "Urgency",
            "Not returned"
        )

        summary = result.get(
            "Summary",
            "Not returned"
        )

        personalized_message = result.get(
            "Personalized_Message",
            "Not returned"
        )

        retail_insight = result.get(
            "Retail_Insight",
            "Not returned"
        )

        # Human-in-the-loop status
        urgency_lower = str(urgency).lower()

        if urgency_lower == "high":
            status = (
                "🔴 **High Urgency — Immediate Human Review Required**"
            )

        elif urgency_lower == "medium":
            status = (
                "🟠 **Medium Urgency — Standard Support Follow-Up Required**"
            )

        else:
            status = (
                "🟢 **Low Urgency — No Immediate Escalation Required**"
            )

        return (
            status,
            category,
            sentiment,
            urgency,
            summary,
            personalized_message,
            retail_insight,
            result
        )

    except Exception as error:
        traceback.print_exc()

        error_message = (
            f"{type(error).__name__}: {error}"
        )

        return (
            f"❌ **Analysis failed:** {error_message}",
            "Error",
            "Error",
            "Error",
            error_message,
            "",
            "",
            {"error": error_message}
        )


